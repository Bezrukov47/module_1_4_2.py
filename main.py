# ОРГАНИЗАЦИЯ ПРОГРАММ И МЕТОДЫ СТРОК
my_string = str("Health of World")
print(len(my_string))
 # Вывод строки в верхнем регистре
print(my_string.upper())
# Вывод строки в нижнем регистре
print(my_string.lower())
# Удаление всех пробелов
my_string_no_spaces = my_string.replace(" ", "")
print(my_string_no_spaces)
# Вывод первого символа
print(my_string[0])
# Вывод последнего символа
print(my_string[-1])